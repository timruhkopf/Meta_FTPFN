class MyRNG:
    """Helper Class to map BNN outputs to parameter supports of the base curves / weights via PIT."""
    def __init__(self, indices, len_ecdf):
        """
        The main purpose of this class is to map the unbounded BNN outputs to the respective parameter's support
        by using the Probability Integral Transform (PIT). It is basically as if the BNN output's had their respective 
        activation functions to meet the parameter's respective support
        """
        self.indices = indices.T # the transpose here allows us to look up all the parameters in the batch for the same parameter index of the BNN output!
        self.len_ecdf = len_ecdf
        self.reset()

    def reset(self):
        self.counter = 0 # This counter basically allows us to step through the parameter index of the BNN output

    def uniform(self, a=0.0, b=1.0):
        u = (b - a) * self.indices[self.counter] / self.len_ecdf + a
        self.counter += 1
        return u

    def normal(self, loc=0, scale=1):
        eps = 0.5 / self.len_ecdf  # to avoid infinite samples
        u = self.uniform(a=eps, b=1 - eps)
        return norm.ppf(u, loc=loc, scale=scale)

    def beta(self, a=1, b=1, loc=0, scale=1):
        eps = 0.5 / self.len_ecdf  # to avoid infinite samples
        u = self.uniform(a=eps, b=1 - eps)
        return beta.ppf(u, a=a, b=b, loc=loc, scale=scale)

    def gamma(self, a=1, loc=0, scale=1):
        eps = 0.5 / self.len_ecdf  # to avoid infinite samples
        u = self.uniform(a=eps, b=1 - eps)
        return gamma.ppf(u, a=a, loc=loc, scale=scale)

    def exponential(self, scale=1):
        eps = 0.5 / self.len_ecdf  # to avoid infinite samples
        u = self.uniform(a=eps, b=1 - eps)
        return expon.ppf(u, scale=scale)


class RelationPrior: # formerly known as DatasetPrior
    output_sorted = None
    CACHE_FILE = Path('.') / "bnn_prior_ecdf.npy"
        
    def _get_BNNs_output_CDF_approximation(self, N_datasets, N_per_dataset, num_outputs):
        """
        Generate and cache a ECDF on the BNN output over the BNN prior.

        This method generates samples from a Bayesian Neural Network (BNN) to approximate 
        the Empirical Cumulative Distribution Function (ECDF) of its output distribution. The samples 
        are collected across multiple datasets and stored globally for subsequent use.

        It is done once during init of training and serves for any subsequent BNN instantiation as y-quantile function.

        Args:
            N_datasets (int): Number of datasets to sample from.
            N_per_dataset (int): Number of samples to generate per dataset.
            num_outputs (int): Dimensionality of the BNN output space.

        Returns:
            None: The method stores the sorted output samples in the class variable 
            `DatasetPrior.output_sorted` for later retrieval.

        Notes:
            - This method is called only once to initialize the CDF approximation cache.
            - A total of N_datasets × N_per_dataset samples are generated (default 1M).
            - Each sample is generated by:
                1. Sampling random uniform input vectors.
                2. Generating a new dataset via `self.new_dataset()`.
                3. Computing BNN output via `self._sample_curve_params()`.
            - The outputs are flattened and sorted to create an empirical CDF.
            - Progress is printed every 100 datasets.
            - The cached CDF is used for quantile estimation and prior sampling.
        """
        if RelationPrior.output_sorted is None:
            # generate 1M samples to approximate the CDF of the BNN output distribution
            output = torch.zeros((N_datasets, N_per_dataset, num_outputs))
            input = torch.from_numpy(
                np.random.uniform(size=(N_datasets, N_per_dataset, self.num_inputs))
            ).to(torch.float32)
            with torch.no_grad():
                for i in range(N_datasets):
                    if i % 100 == 99:
                        print(f"{i+1}/{N_datasets}")
                    # sample a new dataset
                    self.new_dataset()
                    for j in range(N_per_dataset):
                        output_ij = self._map_hp_to_unbounded_curve_params(input[i, j])
                        output[i, j, :] = output_ij
                return np.sort(torch.flatten(output).numpy())


    def __init__(self, num_params, num_outputs, N_datasets = 10000, N_per_dataset = 1, force_recalculate=False):
        """
        Initialize the dataset prior for a Bayesian Neural Network (BNN).

        Parameters:
            num_params (int): The dimension of the hyperparameter space, representing the number of hyperparameters.
            num_outputs (int): The dimension of the BNN's output, indicating how many curve and weight parameters are needed to specify the learning curve surrogate.

            # ECDF approximation quality parameters: 
            # i.e. the number of samples out of the BNN prior to approximate the priors' implied y CDF
            N_datasets (int, optional): The number of datasets to generate. Defaults to 10000.
            N_per_dataset (int, optional): The number of samples per dataset. Defaults to 1.

        This constructor normalizes the BNN inputs using the formula (x - m) / sigma, where 'm' is the mean and 'sigma' is the standard deviation. 

        It also checks if the output of the dataset prior is sorted and, if not, computes the BNN's output CDF approximation using the specified number of datasets and outputs.
        """
        self.num_features = num_params
        self.num_outputs = num_outputs
        self.num_inputs = num_params

        # (x - m) / sigma to normalize BNN inputs
        self.normalizer = Normalize(0.5, math.sqrt(1 / 12))

        if RelationPrior.output_sorted is None:
            # 2. Check if the file exists on disk
            if self.CACHE_FILE.exists() and not force_recalculate:
                print(f"Loading cached CDF from {self.CACHE_FILE}...")
                RelationPrior.output_sorted = np.load(self.CACHE_FILE)
            else:
                print("Calculating BNN output CDF approximation...")
        
                RelationPrior.output_sorted = self._get_BNNs_output_CDF_approximation(
                    N_datasets, N_per_dataset, self.num_outputs
                )
                np.save(self.CACHE_FILE, RelationPrior.output_sorted)
                print(f"CDF approximation saved to {self.CACHE_FILE}")

        self.new_dataset()

    # (Sample surrogate model) ------------------
    def new_dataset(self):
        """
        Reinitialize dataset specific random variables: 

        1. Sample a new BNN surrogate model that defines the mapping from hyperparameters to curve parameters.
        2. Sample initial performance y0 and maximum performance ymax for the curves in this dataset.
        
        :param self: Description
        """
     
        # reinit the parameters of the BNN
        self.model = self._sample_mlp_surrogate()

        # sample the performance range
        self._sample_surrogate_range()

    def _sample_surrogate_range(self):
        # the dataset performance range
        # initial performance (after init) & max performance
        u1 = np.random.uniform()
        u2 = np.random.uniform()
        self.y0 = min(u1, u2)
        self.ymax = max(u1, u2) if np.random.uniform() < 0.25 else 1.0

    def _sample_mlp_surrogate(self):
        return MLP(self.num_inputs, self.num_outputs).to("cpu")

    # (query the surrogate model for an hp's curve parameters) ------------------
    def get_unbounded_curve_params(self, hyperparameters, noise=True):
        # add aleatoric noise & bias
        hyperparameters = torch.from_numpy(hyperparameters)
        output = self._map_hp_to_unbounded_curve_params(hyperparameters)
        return output.numpy()
    
    def _map_hp_to_unbounded_curve_params(self, hyperparams):
        with torch.no_grad():
            # normalize the inputs
            hyperparams = self.normalizer(hyperparams)
            # reweight the inputs for parameter importance
            # hyperparams = hyperparams*self.input_weights  # TODO: consider adding this again
            # apply the model produce the output
            output = self.model(hyperparams.float())
            # rescale and shift outputs to account for parameter sensitivity
            # This output scaling causes issues with
            # output = output * self.output_sensitivity + self.output_offset
            return output
    
    # () --------------------
    def _map_unbounded_bnn_to_bounded_parameter_support(self, rng4config: MyRNG, n_curves=4):
        """
        Based on the unbounded BNN outputs, map them to the respective parameter's support.

        It basically functions as a specific set of activation functions via the Probability Integral Transform (PIT)
        i.e. looking up the global quantile of that BNN output in the respective parameter's marginal distribution, 
        that we define here explicitly.
        
        Includes curve endpoints, weights, shape parameters, saturation points,
        precision values, and noise parameters.
        
        Parameters
        ----------
        rng4config : MyRNG
            Random number generator instance for configuration sampling.
        n_curves : int, optional
            Number of basis curves to sample parameters for. Default is 4. (which is relevant for our 4 basis curves: power_law, exponential, logarithmic, hill) and defines how many weights we sample
        
        Returns
        -------
        tuple
            A tuple containing:
            - Y0 (float): Initial y-value (lower bound).
            - Yinf (float): Asymptotic y-value (upper bound), shared by all components.
            - w (ndarray): Basis curve weights sampled from Dirichlet distribution, shape (n_curves,).
            - alpha (ndarray): Shape/skew parameters for each basis curve, shape (n_curves,).
            - Xsat (ndarray): Saturation x-values for each basis curve, shape (n_curves,).
            - PREC (ndarray): Relative saturation y precision values for each basis curve, shape (n_curves,).
            - Rpsat (ndarray): Post-saturation convergence/divergence rates for each basis curve, shape (n_curves,).
            - sigma (float): Noise standard deviation parameter.

        # carefully investigating the distributions below will make clear, that we have the same number of parameters, 
        # as the bnn has outputs!
        """

        Y0 = self.y0

        # sample Yinf (shared by all components)
        Yinf = rng4config.uniform(a=Y0, b=self.ymax)  # 0

        # sample weights for basis curves (dirichlet)
        w = np.stack([rng4config.gamma(a=1) for i in range(n_curves)]).T  # 1, 2, 3, 4
        w = w / w.sum(axis=1, keepdims=1)

        # sample shape/skew parameter for each basis curve
        alpha = np.stack(
            [
                np.exp(rng4config.normal(1, 1)),  # 5
                np.exp(rng4config.normal(0, 1)),  # 6
                1.0 + np.exp(rng4config.normal(-4, 1)),  # 7
                np.exp(rng4config.normal(0.5, 0.5)),
            ]
        ).T  # 8

        # sample saturation x for each basis curve
        Xsat_max = 10 ** rng4config.normal(0, 1)  # max saturation # 9

        Xsat_rel = np.stack(
            [rng4config.gamma(a=1) for i in range(n_curves)]
        ).T  # relative saturation points # 10, 11, 12, 13

        Xsat = ((Xsat_max.T * Xsat_rel.T) / np.max(Xsat_rel, axis=1)).T

        # sample relative saturation y (PREC) for each basis curve
        PREC = np.stack(
            [1.0 / 10 ** rng4config.uniform(-3, 0) for i in range(n_curves)]
        ).T  # 14, 15, 16, 17

        # post saturation convergence/divergence rate for each basis curve
        Rpsat = np.stack(
            [1.0 - rng4config.exponential(scale=1) for i in range(n_curves)]
        ).T  # 18, 19, 20, 21

        # sample noise parameters
        sigma = np.exp(rng4config.normal(loc=-5, scale=1))
        # sigma_x = np.exp(rng4config.normal(-4,0.5)) # STD of the xGP 22
        # print("warning")
        # sigma_y_scaler = np.exp(rng4config.uniform(-5,0.0)) # STD of the yGP 23
        # L = 10**rng4config.normal(-5,1) # Length-scale of the xyGP 24

        return Y0, Yinf, w, alpha, Xsat, PREC, Rpsat, sigma

    def curves_for_configs(self, configs, noise=True):
        """
        Maps hyperparameter configurations to functional learning curve evaluators.

        This method uses a given BNN surrogate to transform hp configurations into a 
        latent space, which seeds a deterministic sampler to generate the physical 
        parameters for a mixture of four basis functions (Hill, Exp, Log, power_law).
        It will give us a callable that evaluates the learning curves at any fidelity level x for that given 
        set of hp configs.

        Parameter Annotations:
        --------------------
        - Yinf:  The asymptotic performance; the 'plateau' the curve reaches at infinite fidelity.
        - w:     Mixture weights (Dirichlet-like); determines the influence of each basis curve.
        - alpha: Shape/Skew; controls the 'curvature' or how fast the model learns early on.
        - Xsat:  Saturation Point; the fidelity (x-axis) coordinate where the curve begins to flatten.
        - PREC:  Precision/Saturation Height; the relative vertical offset at the saturation point.
        - Rpsat: Post-Saturation Rate; controls if the curve converges or diverges after Xsat.
        - sigma: Observation Noise; the standard deviation of Gaussian noise added to the curve.

        The Role of MyRNG:
        -----------------
        Instead of using standard pseudo-random sampling, this method uses a deterministic 
        Probability Integral Transform (PIT). It maps the BNN's ranked latent outputs 
        directly onto the Quantile Function (PPF) of various distributions. This ensures 
        that a specific hyperparameter configuration always yields a unique, 
        reproducible, and differentiable set of curve parameters.

        Workflow:
        1. Latent Mapping: Maps configs to BNN outputs to drive configuration-specific RNG.
        2. Synthesis: Samples the parameters above based on the BNN-guided latent space.
        3. Evaluation: Returns a closure `foo(x, cid)` which executes the `comb` method—
        plugging these parameters into a weighted mixture of learning curve equations.

        Returns:
            Callable: foo(x, cid) -> clipped [0, 1] performance prediction at fidelity x.
        """
        # more efficient batch-wise
        NCURVES = 4 # the number of basis curves to combine is fixed here!

        # Get the unbounded BNN outputs [-inf, +inf]
        bnn_outputs = self.get_unbounded_curve_params(configs, noise=noise)

        # pass the ecdf to myrng, allowing us to translate the bnn outputs into (global) percentiles
        indices = np.searchsorted(RelationPrior.output_sorted, bnn_outputs, side="left")
        rng4config = MyRNG(indices, len(RelationPrior.output_sorted))

        # Using the rng4configs, we can restrict the output of the bnn to the respective parameter's support,
        # by first defining the PDF for the respective curve parameter (e.g. Xsat ~ Gamma(1,1) ) and then using 
        # the quantile function to map the BNNs output percentiles to the distribution's support.
        Y0, Yinf, w, alpha, Xsat, PREC, Rpsat, sigma = self._map_unbounded_bnn_to_bounded_parameter_support(
            rng4config, n_curves=NCURVES
        )

        def foo(x_, cid=0):
            y_ = comb(
                x_,
                Y0=Y0,
                Yinf=Yinf[cid],
                sigma=None,
                L=None,
                Xsat=Xsat[cid],
                alpha=alpha[cid],
                Rpsat=Rpsat[cid],
                w=w[cid],
                PREC=PREC[cid],
            )
            # y_ = comb(x_, Y0=Y0, Yinf=Yinf[cid], sigma=sigma_x[cid], L=L[cid], Xsat=Xsat[cid], alpha=alpha[cid], Rpsat=Rpsat[cid], w=w[cid], PREC=PREC[cid])
            y_noise = np.random.normal(size=x_.shape, scale=sigma[cid])
            # y_noise = progress_noise(x_,1,L)
            # y_noise *= np.minimum(y_,1.0-y_)/4*sigma_y_scaler[cid]
            return np.clip(y_ + y_noise, 0.0, 1.0)

        return foo



class DimensionPrior:
    def __init__(self, maxnum_dimensions):
        """Determine the number of hyperparameters for the current dataset."""
        assert maxnum_dimensions >= 2
        self.maxnum_dimensions = maxnum_dimensions

    def sample(self):
         # beware upper bound is exclusive!
        return np.random.randint( 1, self.maxnum_dimensions - 1 ) 
    
class FidelityPrior:
    def __init__(self) -> None:
        """Determine the number of fidelity levels for the current dataset."""
        self.n_levels = self.sample()

    def sample(self):
        return int(np.round(10 ** np.random.uniform(0, 3)))

class AllocationPrior:
    EPS = 10**-9

    def __init__(self, seq_len, n_levels) -> None:
        """Determine the allocation of observations/queries per curve in the sequence."""
        self.seq_len = seq_len
        self.n_levels = n_levels
        self.sample_hp()

    def sample_hp(self):
        """Dirichlet-like sampling of allocation weights over the curves in the sequence."""
        self.alpha = 10 ** np.random.uniform(-4, -1)
        self.weights = np.random.gamma(self.alpha, self.alpha, self.seq_len) + self.EPS
        self.p = self.weights / np.sum(self.weights)

    def sample_abstract_allocation(self, single_eval_pos):
        """
        Sample observation and query positions for multiple curves based on a probabilistic ordering.
        This method determines how many observations (cutoff) and total epochs are allocated to each
        curve in the sequence. It uses a weighted sampling approach to assign positions to curves,
        then counts observations and epochs for each curve based on a cutoff position.
        Args:
            single_eval_pos (int): The cutoff position that separates observations from queries.
                Only positions before this index contribute to the observation count for each curve.
        Returns:
            tuple: A tuple containing:
                - cutoff_per_curve (np.ndarray): Integer array of shape (seq_len,) indicating the number
                  of observations allocated to each curve. Values are cumulative counts of positions
                  before single_eval_pos.
                - epochs_per_curve (np.ndarray): Integer array of shape (seq_len,) indicating the total
                  number of epochs (observations + queries) allocated to each curve.
                - ordering (np.ndarray): Integer array of shape (seq_len,) representing the sampled
                  ordering of curve indices for each position in the sequence. Will be used to map positions
                  to curves.
        Notes:
            - Uses self.seq_len to determine sequence length
            - Uses self.n_levels to define granularity of fidelity levels
            - Uses self.p to define base probabilities for each position
            - The method samples positions without replacement according to weighted probabilities
        """
        
        # determine # observations/queries per curve
        # TODO: also make this a dirichlet thing

        ids = np.arange(self.seq_len)
        all_levels = np.repeat(ids, self.n_levels)
        all_p = np.repeat(self.p, self.n_levels) / self.n_levels
        ordering = np.random.choice(all_levels, p=all_p, size=self.seq_len, replace=False)

        # calculate the cutoff/samples for each curve
        cutoff_per_curve = np.zeros((self.seq_len,), dtype=int)
        epochs_per_curve = np.zeros((self.seq_len,), dtype=int)
        for i in range(self.seq_len):  # loop over every pos
            cid = ordering[i]
            epochs_per_curve[cid] += 1
            if i < single_eval_pos:
                cutoff_per_curve[cid] += 1

        return cutoff_per_curve, epochs_per_curve, ordering
    


    def map_(self, curve_configs, curves, num_params, single_eval_pos):

        """Determine x and y values for every curve in the sequence.

        Args:
            curve_configs (np.ndarray): Array of shape (self.seq_len, num_params) containing hyperparameter
                configurations for each curve in the sequence.
            curves (callable): A function that takes x values and a curve index, returning y values.
            single_eval_pos (int, optional): The cutoff position separating observations from queries.
                Defaults to 0.
        Returns:
            tuple: A tuple containing:
                - curve_xs (list): List of length seq_len, where each element is an array of x values
                  for the corresponding curve.
                - curve_ys (list): List of length seq_len, where each element is an array of y values
                  for the corresponding curve.
        """
        cutoff_per_curve, epochs_per_curve, ordering = self.sample_abstract_allocation(single_eval_pos)

        epoch = torch.zeros(self.seq_len)
        id_curve = torch.zeros(self.seq_len)
        curve_val = torch.zeros(self.seq_len)
        config = torch.zeros(self.seq_len, num_params)
        
        curve_xs = []
        curve_ys = []
        for cid in range(self.seq_len):  # loop over every curve
            if epochs_per_curve[cid] > 0:
                # determine x (observations + query)
                x_ = np.zeros((epochs_per_curve[cid],))
                if cutoff_per_curve[cid] > 0:  # observations (if any)
                    x_[: cutoff_per_curve[cid]] = (
                        np.arange(1, cutoff_per_curve[cid] + 1) / self.n_levels
                    )
                if cutoff_per_curve[cid] < epochs_per_curve[cid]:  # queries (if any)
                    x_[cutoff_per_curve[cid] :] = (
                        np.random.choice(
                            np.arange(cutoff_per_curve[cid] + 1, self.n_levels + 1),
                            size=epochs_per_curve[cid] - cutoff_per_curve[cid],
                            replace=False,
                        )
                        / self.n_levels
                    )
                curve_xs.append(x_)
                # determine y's
                y_ = curves(x_, cid)
                curve_ys.append(y_)
            else:
                curve_xs.append(None)
                curve_ys.append(None)

        # construct the batch data element
        curve_counters = torch.zeros(self.seq_len).type(torch.int64)
        for i in range(self.seq_len):
            cid = ordering[i]
            if i < single_eval_pos or curve_counters[cid] > 0:
                id_curve[i] = cid + 1  # reserve ID 0 for queries
            else:
                id_curve[i] = 0  # queries for unseen curves always have ID 0
            epoch[i] = curve_xs[cid][curve_counters[cid]]
            config[i] = torch.from_numpy(curve_configs[cid])
            curve_val[i] = curve_ys[cid][curve_counters[cid]]
            curve_counters[cid] += 1

        x = torch.cat([torch.stack([id_curve, epoch], dim=1), config], dim=1)
        y = curve_val

        return x, y

    

# function producing batches for PFN training
@torch.no_grad()
def get_batch(
    batch_size,
    seq_len,
    num_features,
    single_eval_pos,
    device=default_device,
    hyperparameters=None,
    **kwargs,
):
 
    num_params = DimensionPrior(num_features).sample()

    dataset_prior = RelationPrior(num_params, 23)

    x = []
    y = []

    for i in range(batch_size):
        

        # determine the number of fidelity levels (ranging from 1: BB, up to seq_len)
        n_levels = FidelityPrior().sample()
        # print(f"n_levels: {n_levels}")

        # determine # observations/queries per curve
        # TODO: also make this a dirichlet thing
        allocation_prior = AllocationPrior(seq_len, n_levels)
        
        # fix dataset specific random variables
        # i.e. get a new relation prior
        dataset_prior.new_dataset()

        # determine config, x, y for every curve -----
        # (1) sample "available" hyperparameter configurations, these will later be subselected and
        # determined to be either observation or query points
        # FIXME: move this into the allocation prior, since it is basically an internal representation!
        curve_configs = np.random.uniform(size=(seq_len, num_params)) 

        # (2) get the curves for these configurations
        curves = dataset_prior.curves_for_configs(curve_configs)  # get callable to evaluate (hp, t) --> y

        # (3) map the allocation to actual (x,y) values
        x_i, y_i = allocation_prior.map_(curve_configs, curves, num_params, single_eval_pos)

        x.append(x_i)
        y.append(y_i)

    x = torch.stack(x, dim=1).to(device).float()
    y = torch.stack(y, dim=1).to(device).float()

    return Batch(x=x, y=y, target_y=y)

